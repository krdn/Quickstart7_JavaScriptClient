            //.AddCookie()
            //.AddOpenIdConnect(options =>
            //{
            //    options.Authority = Configuration["IdentityServerAddress"];
            //    options.RequireHttpsMetadata = false;

            //    options.ClientId = "mvc";
            //    options.ClientSecret = "secret";

